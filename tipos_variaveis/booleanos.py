# Condição verdadeira
True
# Condição falsa
False

# Condicionais são blocos de código que são executados caso a condição seja atendida
if True:
    print("bloco IF vai ser executado")
else:
    print("bloco ELSE vai ser executado")


# Operadores lógicos: and e or
    
if True and True:
    print("Bloco será executado") # Operado and

if True and False:
    print("Bloco não será executado") # Operado and


if True or False:
    print("O bloco será executado")
if True or True:
    print("O bloco será executado")
if False or False:
    print("O bloco não será executado")