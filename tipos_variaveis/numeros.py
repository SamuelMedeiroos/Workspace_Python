# Inteiro
numero_inteiro = 3
print(f"Inteiro = {numero_inteiro}")

# Real com pónto flutuante
numero_real = 3.14
print(f"Real com ponto flutuante = {numero_real}")

# type()
print(f"Tipo da variavel inteiro = {type(numero_inteiro)}")
print(f"Tipo da variavel real com ponto flutuante = {type(numero_real)}")

# soma +
soma = 1 + 1
print(f"1 + 1 = {soma}")
print(f"Tipo da variavel do resultado = {type(soma)}")


# subtração -
subtracao = 1 - 1
print(f"1 - 1 = {subtracao}")
print(f"Tipo da variavel do resultado = {type(subtracao)}")


# multiplicação *
multiplicacao = 2 * 2
print(f"2 x 2 = {multiplicacao}")
print(f"Tipo da variavel do resultado = {type(multiplicacao)}")


# divisão /
divisao = 5 / 2
print(f"5 / 2 = {divisao}")
print(f"Tipo da variavel do resultado = {type(divisao)}") 
# a operação de divisão sempre retorna um float

# realizando transformação de dados
print(f"O resultado da divisão transformada em int é = {int(divisao)}")

# módulo %
modulo = 5 % 2
print(f"Modulo = {modulo}")

# divisão inteira
divisao_inteira = 5 // 2
print(f"a divisão inteira 5 // 2 = {divisao_inteira}")